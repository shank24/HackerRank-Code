package learning.basic.DataType;

public
class BitwiseDemo {
    public static
    void main (String[] args) {

        System.out.println ( true & false );

        System.out.println ( true | false );

        System.out.println ( true ^ false );

        System.out.println ( 4 & 5 );

        // Either of one is true
        System.out.println ( 4 | 5 );

        System.out.println ( 4 ^ 5 );
        System.out.println (~4 );

    }
}
